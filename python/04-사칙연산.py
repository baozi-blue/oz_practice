# 더하기 +
print(2+3)

# 빼기  -
print(5-4)

# 곱하기  *
print(3*4)

# 거듭제곱 **
print(2**10)

# 나누기 
# / : 5/2 = 2.5 
# // : 5/2 = 2 몱을 반환
# % : 5/2 = 1 나머지를 반환
print(4/2)
print(4//2)
print(4%2)

# 내장함수 (자주 사용하는 기능을 미리 만들어 놓을 것들)
# divmod : 목과 나머지를 동시에 구해주는 내장함수

print(divmod(3,2)) 

# 괄호 () 안에 있는 연산 먼저 괄호 -> 곱셉, 나눕셉 -> 덧셈, 뺄셈
(4*5)-10




